package com.thuduyen07.C4_JavaVariables;

/**
 * Widening: converting a lower datatype to a higher datatype
 */
public class Widening {
    public static void main(String[] args) {
        int a = 10;
        float f = a;
        System.out.println("a = " + a);
        System.out.println("f = " + f);
    }
}
